package com.project.newsapp;

public class java_one {
    int num;
    String word;

    public java_one(int num, String word) {
        this.num = num;
        this.word = word;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
